package com.fladioarmandika.tridaya.model;

public class Jadwal {

	private int id;
	private String hari;
	private int jam;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getHari() {
		return hari;
	}
	public void setHari(String hari) {
		this.hari = hari;
	}
	public int getJam() {
		return jam;
	}
	public void setJam(int jam) {
		this.jam = jam;
	}
	
	
	
}
