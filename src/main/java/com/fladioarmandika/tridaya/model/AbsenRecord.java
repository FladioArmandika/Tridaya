package com.fladioarmandika.tridaya.model;

public class AbsenRecord {

	private int id;
	private int idPembelajaran;
	private int idSiswa;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getIdPembelajaran() {
		return idPembelajaran;
	}
	public void setIdPembelajaran(int idPembelajaran) {
		this.idPembelajaran = idPembelajaran;
	}
	public int getIdSiswa() {
		return idSiswa;
	}
	public void setIdSiswa(int idSiswa) {
		this.idSiswa = idSiswa;
	}
	
	
	
}
